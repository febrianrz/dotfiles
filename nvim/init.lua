-- Tutup semua buffer
local function close_all_buffers()
  for _, buf in ipairs(vim.api.nvim_list_bufs()) do
    if vim.api.nvim_buf_is_loaded(buf) and vim.api.nvim_buf_get_option(buf, 'buflisted') then
      vim.api.nvim_buf_delete(buf, {})
    end
  end
end

-- Tutup semua buffer kecuali yang aktif
local function close_other_buffers()
  local current = vim.fn.bufnr()
  for _, buf in ipairs(vim.api.nvim_list_bufs()) do
    if vim.api.nvim_buf_is_loaded(buf) and vim.api.nvim_buf_get_option(buf, 'buflisted') and buf ~= current then
      vim.api.nvim_buf_delete(buf, {})
    end
  end
end

-- Tutup buffer di sebelah kiri
local function close_left_buffers()
  local current = vim.fn.bufnr()
  for _, buf in ipairs(vim.api.nvim_list_bufs()) do
    if buf < current and vim.api.nvim_buf_is_loaded(buf) and vim.api.nvim_buf_get_option(buf, 'buflisted') then
      vim.api.nvim_buf_delete(buf, {})
    end
  end
end

-- Tutup buffer di sebelah kanan
local function close_right_buffers()
  local current = vim.fn.bufnr()
  for _, buf in ipairs(vim.api.nvim_list_bufs()) do
    if buf > current and vim.api.nvim_buf_is_loaded(buf) and vim.api.nvim_buf_get_option(buf, 'buflisted') then
      vim.api.nvim_buf_delete(buf, {})
    end
  end
end

